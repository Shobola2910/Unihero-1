from datetime import datetime, timezone
from textwrap import dedent
from contextlib import contextmanager

from apps.shared.db import init_db, get_session, InlineBlock

@contextmanager
def session_scope():
    s = get_session()
    try:
        yield s
        s.commit()
    except Exception:
        s.rollback()
        raise
    finally:
        s.close()

def upsert(key: str, title: str, uz: str, en: str, ru: str):
    now = datetime.now(timezone.utc).replace(tzinfo=None)
    with session_scope() as s:
        b = s.query(InlineBlock).filter(InlineBlock.key == key).one_or_none()
        if b is None:
            b = InlineBlock(
                key=key, title=title,
                # ‚ö†Ô∏è Legacy lang ustuni uchun default qiymat ham ketadi
                lang="uz",
                body_uz=uz, body_en=en, body_ru=ru,
                created_at=now, updated_at=now
            )
            s.add(b)
        else:
            b.title    = title
            b.body_uz  = uz
            b.body_en  = en
            b.body_ru  = ru
            b.updated_at = now

